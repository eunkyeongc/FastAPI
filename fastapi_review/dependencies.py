from database import SessionLocal

def get_db():
    """DB 세션을 만들어서 엔트포인트에 주입해주는 함수
        엔드포인트 함수에서  db: Session = Depends(get_db)로 사용

    세션 : DB와 주고받는 대화창구

    예)
    DB(데이터베이스) == 도서관
    엔진(engine) == 도서관 건물 자체(한 번만 만들고 계속 사용)
    세션(session) == 도서관 직원과의 대화 한 번(용무 한 번)

    return == 직원이 책 주고 도서관 문 닫기
    yield == 직원이 책 빌려주고 --> 손님이 다 볼때까지 기다렸다가 --> 반납하고 정리
    """
    db = SessionLocal()     # 새 DB 세션 생성(요청마다 새로 만들고 끝나면 닫는다)
    try:
        yield db    # db를 넘겨주고 여기서 멈춘다. --> 엔드포인트 함수 실행 --> 이어서 실행
    finally:
        db.close()  # 앤드포인트 성공/실패 관계없이 받드시 세션 닫는다. 메모리 누수 방지용.

